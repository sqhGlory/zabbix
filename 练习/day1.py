'''
标识符可以由字母、数字和下划线组成，但是不能以数字开头。

标识符不能包含空格或其他特殊字符，例如 @、#、$ 等。

标识符是区分大小写的，例如 my_variable 和 My_Variable 是不同的标识符。

Python 中有一些保留字，不能用作标识符，例如 if、else、while 等。

在 Python 中，为了让标识符更加具有可读性和可维护性，通常遵守以下命名约定：

变量名和函数名通常使用小写字母，可以使用下划线分隔单词，例如 my_variable、my_function。

类名通常使用大写字母开头的驼峰命名法，例如 MyClass。

模块名通常使用小写字母，可以使用下划线分隔单词，例如 my_module。

常量名通常使用大写字母，可以使用下划线分隔单词，例如 MY_CONSTANT


整数（int）：表示整数，例如 1、2、3 等。

浮点数（float）：表示带小数的数字，例如 1.0、3.14、-2.5 等。

布尔值（bool）：表示真或假，只有两个值 True 和 False。

字符串（str）：表示文本，例如 "hello"、"world" 等。

列表（list）：表示一组有序的元素，可以包含不同类型的数据，例如 [1, 2, "hello", True] 等。

元组（tuple）：与列表类似，但是元素不能被修改，例如 (1, 2, "hello", True)。

集合（set）：表示一组互不相同的元素，例如 {1, 2, 3}。

字典（dict）：表示一组键值对，例如 {"name": "Alice", "age": 25}。

除了这些内置数据类型，Python 还支持许多其他数据类型，例如日期和时间类型、字节串类型、空值类型等。在编写 Python 程序时，选择合适的数据类型可以使程序更加简洁和高效。
'''
